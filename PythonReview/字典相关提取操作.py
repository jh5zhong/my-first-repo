#提取所有关键字（冒号前的数据）
A = {"语文":92,"数学":91,"英语":93,"物理":118,"化学":139}
for key in A.keys():
    print(key,end=' ')
print('\n')  
#提取所有值（冒号后的数据）
A = {"语文":92,"数学":91,"英语":93,"物理":118,"化学":139}
for value in A.values():
    print(value,end=' ')  
print('\n')    
#提取所有键值对（字典中的每一组数据）
A = {"语文":92,"数学":91,"英语":93,"物理":118,"化学":139}
for item in A.items():
    print(item)